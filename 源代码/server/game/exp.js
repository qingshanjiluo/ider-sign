// GROWTH_TABLE[lv] = 达到 lv 级获得的属性成长 (lv: 2..400)
const GROWTH_TABLE = new Array(401).fill(0);

(function _precomputeGrowth() {
  function _oldGrowthForTier(t) {
    if (t <= 12) return 2;
    if (t <= 15) return 10;
    if (t === 16) return 15;
    if (t <= 19) return 50;
    if (t === 20) return 80;
    if (t <= 23) return 120;
    if (t === 24) return 180;
    if (t <= 27) return 200;
    if (t === 28) return 250;
    if (t <= 31) return 280;
    if (t === 32) return 350;
    if (t <= 35) return 400;
    if (t === 36) return 500;
    if (t <= 39) return 550;
    return 650;
  }
  for (let lv = 2; lv <= 400; lv++) {
    const tier = Math.min(40, Math.max(1, Math.floor((lv + 9) / 10)));
    const g = _oldGrowthForTier(tier);
    const pos = (lv - 1) % 10;
    if (g < 10) {
      GROWTH_TABLE[lv] = pos === 4 ? g : 0;
    } else {
      const base = Math.floor(g / 10);
      const r = g % 10;
      GROWTH_TABLE[lv] = base + (pos < r ? 1 : 0);
    }
  }
})();

function getEffectiveTier(level) {
  return Math.min(40, Math.max(1, Math.floor((level + 9) / 10)));
}

function calculateExpNeeded(level) {
  if (level <= 0) return 0;
  if (level >= 400) return EXP_TABLE[399];
  return EXP_TABLE[level];
}

function getGrowthForLevel(level) {
  if (level < 2 || level > 400) return 0;
  return GROWTH_TABLE[level];
}

/** 计算玩家总经验（用于坊市交易限制等）：达到当前等级所需经验之和 + 当前未消耗经验 */
function getTotalExp(player) {
  if (!player) return 0;
  const lv = Math.max(1, Math.min(400, Math.floor(Number(player.level) || 1)));
  const currentExp = Math.max(0, Math.floor(Number(player.exp) || 0));
  let sum = 0;
  for (let i = 1; i < lv; i++) {
    if (i < EXP_TABLE.length) sum += EXP_TABLE[i];
  }
  return sum + currentExp;
}

